// Port of the desktop ScriptGenerator. Output must match the WPF app so a sequence
// produces the same script whichever tool built it.

function stepTypeName(step) {
    return (STEP_TYPES[step.Type] && STEP_TYPES[step.Type].label) || step.Type;
}

function ensureExe(name) {
    return /\.exe$/i.test(name) ? name : name + ".exe";
}

function escapePs(s) {
    return String(s).replace(/'/g, "''");
}

// Escape a line for use inside a batch ECHO; mirrors the desktop EscapeBatchEcho.
function escapeBatchEcho(line) {
    if (!line) return ".";
    return line
        .replace(/%/g, "%%")
        .replace(/\^/g, "^^")
        .replace(/&/g, "^&")
        .replace(/</g, "^<")
        .replace(/>/g, "^>")
        .replace(/\|/g, "^|");
}

function timestamp() {
    const d = new Date();
    const p = n => String(n).padStart(2, "0");
    return `${d.getFullYear()}-${p(d.getMonth() + 1)}-${p(d.getDate())} ${p(d.getHours())}:${p(d.getMinutes())}`;
}

// ── .bat ──────────────────────────────────────────────────────────────────

function generateBat(seq) {
    const out = [];
    out.push("@echo off");
    out.push("setlocal enabledelayedexpansion");
    out.push(`:: ${seq.name}`);
    out.push(`:: Generated by LaunchForge on ${timestamp()}`);
    out.push("");

    seq.steps.forEach((step, i) => {
        const comment = step.label ? `${stepTypeName(step)}: ${step.label}` : stepTypeName(step);
        if (!step.isEnabled) {
            out.push(`:: [DISABLED] ${comment}`);
            out.push("");
            return;
        }
        out.push(`:: Step ${i + 1}: ${comment}`);
        out.push(toBatLine(step));
        out.push("");
    });

    out.push("echo All done.");
    return out.join("\r\n");
}

function toBatLine(step) {
    const s = step;
    switch (s.Type) {
        case "LaunchApp": {
            const args = s.arguments ? " " + s.arguments : "";
            return s.waitForExit
                ? `start /wait "" "${s.exePath}"${args}`
                : `start "" "${s.exePath}"${args}`;
        }
        case "LaunchSteam":
            return `start "" "steam://rungameid/${s.appId}"`;
        case "Wait":
            // ping the loopback as a sleep: N+1 pings is ~N seconds, and unlike
            // `timeout` it doesn't read console stdin (which other apps can corrupt)
            return `ping -n ${s.seconds + 1} 127.0.0.1 >nul`;
        case "KillProcess":
            return `taskkill /f /im "${ensureExe(s.processName)}"`;
        case "WaitForProcessStart":
            return s.timeoutSeconds > 0
                ? `powershell -Command "$proc = '${s.processName}'; $timeout = ${s.timeoutSeconds}; $start = Get-Date; ` +
                  `while ($true) { if (Get-Process $proc -ErrorAction SilentlyContinue) { break }; ` +
                  `if (((Get-Date) - $start).TotalSeconds -ge $timeout) { Write-Warning ('Timed out waiting for ' + $proc); break }; ` +
                  `Start-Sleep -Milliseconds 500 }"`
                : `powershell -Command "$proc = '${s.processName}'; ` +
                  `while (-not (Get-Process $proc -ErrorAction SilentlyContinue)) { Start-Sleep -Milliseconds 500 }"`;
        case "WaitForProcessExit":
            return s.timeoutSeconds > 0
                ? `powershell -Command "$proc = '${s.processName}'; $timeout = ${s.timeoutSeconds}; $start = Get-Date; ` +
                  `while (Get-Process $proc -ErrorAction SilentlyContinue) { ` +
                  `if (((Get-Date) - $start).TotalSeconds -ge $timeout) { Write-Warning ('Timed out waiting for ' + $proc + ' to exit'); break }; ` +
                  `Start-Sleep -Milliseconds 500 }"`
                : `powershell -Command "$proc = '${s.processName}'; ` +
                  `while (Get-Process $proc -ErrorAction SilentlyContinue) { Start-Sleep -Milliseconds 500 }"`;
        case "RunPowerShell":
            return writeTempPs1Bat(s, !s.waitForExit);
        default:
            return `:: (unknown step type: ${s.Type})`;
    }
}

function writeTempPs1Bat(s, noWait) {
    const lines = [];
    lines.push("setlocal");
    lines.push("set _tmpfile=%TEMP%\\lf_ps_%RANDOM%.ps1");
    for (const line of String(s.script).replace(/\r\n/g, "\n").split("\n")) {
        lines.push(`echo ${escapeBatchEcho(line)}>> %_tmpfile%`);
    }
    lines.push(noWait
        ? "start /b powershell -ExecutionPolicy Bypass -File %_tmpfile%"
        : "powershell -ExecutionPolicy Bypass -File %_tmpfile%");
    lines.push("del %_tmpfile%");
    lines.push("endlocal");
    return lines.join("\r\n");
}

// ── .ps1 ──────────────────────────────────────────────────────────────────

function generatePs1(seq) {
    const out = [];
    out.push("#Requires -Version 5");
    out.push("Set-StrictMode -Version Latest");
    out.push(`# ${seq.name}`);
    out.push(`# Generated by LaunchForge on ${timestamp()}`);
    out.push("");

    seq.steps.forEach((step, i) => {
        const comment = step.label ? `${stepTypeName(step)}: ${step.label}` : stepTypeName(step);
        if (!step.isEnabled) {
            out.push(`# [DISABLED] ${comment}`);
            out.push("");
            return;
        }
        out.push(`# Step ${i + 1}: ${comment}`);
        out.push(toPs1Block(step));
        out.push("");
    });

    out.push("Write-Host 'All done.'");
    return out.join("\r\n");
}

function toPs1Block(step) {
    const s = step;
    switch (s.Type) {
        case "LaunchApp": {
            const args = s.arguments ? ` -ArgumentList '${escapePs(s.arguments)}'` : "";
            return `Start-Process -FilePath '${escapePs(s.exePath)}'${args}${s.waitForExit ? " -Wait" : ""}`;
        }
        case "LaunchSteam":
            return `Start-Process 'steam://rungameid/${s.appId}'`;
        case "Wait":
            return `Start-Sleep -Seconds ${s.seconds}`;
        case "KillProcess":
            return `Stop-Process -Name '${escapePs(s.processName)}' -Force -ErrorAction SilentlyContinue`;
        case "WaitForProcessStart":
            return s.timeoutSeconds > 0
                ? [
                    `$_proc = '${escapePs(s.processName)}'; $_timeout = ${s.timeoutSeconds}; $_start = Get-Date`,
                    "while ($true) {",
                    "    if (Get-Process $_proc -ErrorAction SilentlyContinue) { break }",
                    `    if (((Get-Date) - $_start).TotalSeconds -ge $_timeout) { Write-Warning "Timed out waiting for $_proc to start"; break }`,
                    "    Start-Sleep -Milliseconds 500",
                    "}",
                  ].join("\r\n")
                : [
                    `$_proc = '${escapePs(s.processName)}'`,
                    "while (-not (Get-Process $_proc -ErrorAction SilentlyContinue)) {",
                    "    Start-Sleep -Milliseconds 500",
                    "}",
                  ].join("\r\n");
        case "WaitForProcessExit":
            return s.timeoutSeconds > 0
                ? [
                    `$_proc = '${escapePs(s.processName)}'; $_timeout = ${s.timeoutSeconds}; $_start = Get-Date`,
                    "while (Get-Process $_proc -ErrorAction SilentlyContinue) {",
                    `    if (((Get-Date) - $_start).TotalSeconds -ge $_timeout) { Write-Warning "Timed out waiting for $_proc to exit"; break }`,
                    "    Start-Sleep -Milliseconds 500",
                    "}",
                  ].join("\r\n")
                : [
                    `$_proc = '${escapePs(s.processName)}'`,
                    "while (Get-Process $_proc -ErrorAction SilentlyContinue) {",
                    "    Start-Sleep -Milliseconds 500",
                    "}",
                  ].join("\r\n");
        case "RunPowerShell":
            return s.waitForExit
                ? ["& {", s.script, "}"].join("\r\n")
                : ["Start-Job -ScriptBlock {", s.script, "} | Out-Null"].join("\r\n");
        default:
            return `# (unknown step type: ${s.Type})`;
    }
}
