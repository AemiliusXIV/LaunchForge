// Import parser. Accepts .lfjson, .bat, or .ps1, auto-detects the format, and
// turns it back into a sequence. Scripts LaunchForge exported round-trip exactly;
// common hand-written batch/PowerShell patterns are recognised too. Whatever it
// can't read is collected as a warning rather than dropped silently.

const Parser = (() => {

    function detectFormat(filename, text) {
        const ext = (filename.split(".").pop() || "").toLowerCase();
        if (ext === "lfjson" || ext === "json") return "lfjson";
        if (ext === "bat" || ext === "cmd") return "bat";
        if (ext === "ps1") return "ps1";

        // No telling extension: sniff the content.
        const t = text.trimStart();
        if (t.startsWith("{")) return "lfjson";
        if (/^@echo\s+off/im.test(text) || /^\s*timeout\s+\/t/im.test(text) || /^\s*start\s+""/im.test(text)) return "bat";
        if (/Start-Sleep|Start-Process|#Requires/i.test(text)) return "ps1";
        return "lfjson";
    }

    function importAny(filename, text) {
        const fmt = detectFormat(filename, text);
        const result = fmt === "lfjson" ? parseLfjson(text)
                     : fmt === "bat"    ? parseBat(text)
                     :                    parsePs1(text);
        result.format = fmt;
        if (!result.name) {
            const base = (filename.split(/[\\/]/).pop() || "").replace(/\.[^.]+$/, "");
            result.name = base || "Imported sequence";
        }
        return result;
    }

    // ── shared helpers ──

    function add(steps, type, fields) {
        const s = makeStep(type);
        Object.assign(s, fields);
        steps.push(s);
        return s;
    }

    const stripExe = n => n.replace(/\.exe$/i, "");

    function extractName(text, re) {
        for (const l of text.split(/\r?\n/)) {
            const m = l.match(re);
            if (!m) continue;
            const c = m[1].trim();
            if (/^Generated by LaunchForge/i.test(c)) continue;
            if (/^Step\s+\d+:/i.test(c)) continue;
            if (/^\[DISABLED\]/i.test(c)) continue;
            return c;
        }
        return "";
    }

    // ── .lfjson (with repair) ──

    function parseLfjson(text) {
        const warnings = [];
        let obj;
        try { obj = JSON.parse(text); }
        catch (e) { return { name: "", steps: [], warnings: ["This file isn't valid JSON: " + e.message] }; }

        const raw = Array.isArray(obj.steps) ? obj.steps : [];
        const steps = [];
        raw.forEach((r, i) => {
            const fixed = repairStep(r, i, warnings);
            if (fixed) steps.push(fixed);
        });
        return { name: typeof obj.name === "string" ? obj.name : "", steps, warnings };
    }

    function repairStep(raw, i, warnings) {
        if (!raw || typeof raw !== "object") { warnings.push(`Step ${i + 1}: not an object, skipped.`); return null; }
        const type = raw.Type || raw.type;
        if (!type || !STEP_TYPES[type]) { warnings.push(`Step ${i + 1}: unknown type "${type}", skipped.`); return null; }

        const step = makeStep(type);
        step.label = typeof raw.label === "string" ? raw.label : "";
        step.isEnabled = raw.isEnabled === undefined ? true : !!raw.isEnabled;

        for (const field of STEP_TYPES[type].fields) {
            const v = raw[field.key];
            if (v === undefined || v === null) continue;
            if (field.type === "number") {
                const n = Number(v);
                if (Number.isFinite(n)) step[field.key] = n;
            } else if (field.type === "checkbox") {
                step[field.key] = !!v;
            } else {
                step[field.key] = String(v);
            }
        }
        return step;
    }

    // ── .bat ──

    function unescapeBatchEcho(s) {
        if (s === ".") return "";
        return s.replace(/\^&/g, "&").replace(/\^</g, "<").replace(/\^>/g, ">")
                .replace(/\^\|/g, "|").replace(/\^\^/g, "^").replace(/%%/g, "%");
    }

    function parseBat(text) {
        const warnings = [];
        const steps = [];
        const lines = text.split(/\r?\n/);
        let label = "";

        for (let i = 0; i < lines.length; i++) {
            const line = lines[i].trim();
            if (!line) continue;
            const low = line.toLowerCase();
            let m;

            // Our generator's per-step comment carries the label
            if (m = line.match(/^::\s*Step\s+\d+:\s*(.+)$/i)) {
                const c = m[1];
                const idx = c.indexOf(":");
                label = idx >= 0 ? c.slice(idx + 1).trim() : "";
                continue;
            }
            // Boilerplate and comments
            if (low.startsWith("::") || low.startsWith("rem ") || low === "@echo off" ||
                low.startsWith("setlocal") || low === "endlocal" ||
                low.startsWith("echo") || low.startsWith("del ")) {
                continue;
            }

            // RunPowerShell temp-file block
            if (low.startsWith("set _tmpfile=")) {
                const scriptLines = [];
                let waitForExit = true;
                i++;
                for (; i < lines.length; i++) {
                    const l = lines[i].trim();
                    let mm;
                    if (mm = l.match(/^echo\s+(.*?)\s*>>\s*%_tmpfile%$/i)) {
                        scriptLines.push(unescapeBatchEcho(mm[1]));
                    } else if (/powershell.*-File\s+%_tmpfile%/i.test(l)) {
                        waitForExit = !/\bstart\s+\/b\b/i.test(l);
                    } else if (l.toLowerCase() === "endlocal") {
                        break;
                    } else if (/^del\s+%_tmpfile%/i.test(l) || !l) {
                        // part of the block, ignore
                    } else {
                        i--; break;
                    }
                }
                add(steps, "RunPowerShell", { script: scriptLines.join("\n"), waitForExit, label });
                label = ""; continue;
            }

            // Launch Steam
            if (m = line.match(/^start\s+(?:\/\w+\s+)*""\s+"steam:\/\/rungameid\/(\d+)"/i)) {
                add(steps, "LaunchSteam", { appId: m[1], gameName: Steam.nameForAppId(m[1]) || "", label });
                label = ""; continue;
            }
            // Launch App:  start [/flags] ["title"] "path" [args]
            if (m = line.match(/^start\s+((?:\/\w+\s+)*)(?:"[^"]*"\s+)?"([^"]+)"\s*(.*)$/i)) {
                add(steps, "LaunchApp", {
                    exePath: m[2],
                    arguments: (m[3] || "").trim(),
                    waitForExit: /\/wait/i.test(m[1] || ""),
                    label,
                });
                label = ""; continue;
            }
            // Wait
            if (m = line.match(/^timeout\s+\/t\s+(\d+)/i)) {
                add(steps, "Wait", { seconds: Number(m[1]), label });
                label = ""; continue;
            }
            // Hand-written ping delay trick
            if (m = line.match(/^ping\b.*-n\s+(\d+)/i)) {
                add(steps, "Wait", { seconds: Math.max(1, Number(m[1]) - 1), label });
                label = ""; continue;
            }
            // Kill process
            if (m = line.match(/^taskkill\s+.*\/im\s+"?([^"\s]+)"?/i)) {
                add(steps, "KillProcess", { processName: stripExe(m[1]), label });
                label = ""; continue;
            }
            // Inline powershell: our wait loops, else generic
            if (low.startsWith("powershell")) {
                const wait = parsePsWaitInline(line);
                if (wait) { wait.label = label; steps.push(wait); label = ""; continue; }
                const inner = line.match(/-Command\s+"([\s\S]*)"\s*$/i);
                if (inner) { add(steps, "RunPowerShell", { script: inner[1], waitForExit: true, label }); label = ""; continue; }
            }

            warnings.push(`Couldn't parse: ${line}`);
            label = "";
        }
        return { name: extractName(text, /^::\s*(.+)$/), steps, warnings };
    }

    function parsePsWaitInline(line) {
        const pm = line.match(/\$proc\s*=\s*'([^']+)'/);
        if (!pm) return null;
        const tm = line.match(/\$timeout\s*=\s*(\d+)/);
        const timeout = tm ? Number(tm[1]) : 0;

        let kind = null;
        if (/while\s*\(\s*-not\s*\(Get-Process/i.test(line)) kind = "start";
        else if (/if\s*\(Get-Process[^)]*\)\s*\{\s*break/i.test(line)) kind = "start";
        else if (/while\s*\(\s*Get-Process/i.test(line)) kind = "exit";
        else return null;

        const s = makeStep(kind === "start" ? "WaitForProcessStart" : "WaitForProcessExit");
        s.processName = pm[1];
        s.timeoutSeconds = timeout;
        return s;
    }

    // ── .ps1 ──

    function collectBlock(lines, startIdx) {
        let depth = 0;
        const body = [];
        let end = startIdx;
        for (let j = startIdx; j < lines.length; j++) {
            const line = lines[j];
            const before = depth;
            for (const ch of line) { if (ch === "{") depth++; else if (ch === "}") depth--; }
            if (j === startIdx) {
                const after = line.slice(line.indexOf("{") + 1);
                if (depth <= 0) { body.push(after.slice(0, after.lastIndexOf("}")).trim()); end = j; break; }
                if (after.trim()) body.push(after);
            } else if (depth <= 0) {
                const seg = line.slice(0, line.lastIndexOf("}")).trim();
                if (seg) body.push(seg);
                end = j; break;
            } else {
                body.push(line);
            }
            end = j;
        }
        return { body: body.join("\n").trim(), end };
    }

    function parsePs1(text) {
        const warnings = [];
        const steps = [];
        const lines = text.split(/\r?\n/);
        let label = "";

        for (let i = 0; i < lines.length; i++) {
            const line = lines[i].trim();
            if (!line) continue;
            const low = line.toLowerCase();
            let m;

            if (m = line.match(/^#\s*Step\s+\d+:\s*(.+)$/i)) {
                const c = m[1];
                const idx = c.indexOf(":");
                label = idx >= 0 ? c.slice(idx + 1).trim() : "";
                continue;
            }
            if (low.startsWith("#") || low.startsWith("set-strictmode") || /^write-host\s+'all done\.'/i.test(line)) continue;

            if (m = line.match(/^Start-Process\s+'steam:\/\/rungameid\/(\d+)'/i)) {
                add(steps, "LaunchSteam", { appId: m[1], gameName: Steam.nameForAppId(m[1]) || "", label });
                label = ""; continue;
            }
            if (m = line.match(/^Start-Process\s+-FilePath\s+'((?:[^']|'')*)'(.*)$/i)) {
                const rest = m[2] || "";
                const am = rest.match(/-ArgumentList\s+'((?:[^']|'')*)'/i);
                add(steps, "LaunchApp", {
                    exePath: m[1].replace(/''/g, "'"),
                    arguments: am ? am[1].replace(/''/g, "'") : "",
                    waitForExit: /-Wait\b/i.test(rest),
                    label,
                });
                label = ""; continue;
            }
            if (m = line.match(/^Start-Sleep\s+-Seconds\s+(\d+)/i)) {
                add(steps, "Wait", { seconds: Number(m[1]), label });
                label = ""; continue;
            }
            if (m = line.match(/^Stop-Process\s+-Name\s+'((?:[^']|'')*)'/i)) {
                add(steps, "KillProcess", { processName: stripExe(m[1].replace(/''/g, "'")), label });
                label = ""; continue;
            }
            // Process-wait loop: starts with the $_proc prelude
            if (m = line.match(/^\$_proc\s*=\s*'((?:[^']|'')*)'/i)) {
                const proc = m[1].replace(/''/g, "'");
                const tm = line.match(/\$_timeout\s*=\s*(\d+)/);
                const timeout = tm ? Number(tm[1]) : 0;

                let kind = null, depth = 0, started = false;
                for (let j = i; j < lines.length; j++) {
                    const lj = lines[j];
                    if (kind === null) {
                        if (/while\s*\(\s*-not\s*\(Get-Process/i.test(lj)) kind = "start";
                        else if (/while\s*\(\s*\$true/i.test(lj)) kind = "start";
                        else if (/while\s*\(\s*Get-Process/i.test(lj)) kind = "exit";
                    }
                    for (const ch of lj) { if (ch === "{") { depth++; started = true; } else if (ch === "}") depth--; }
                    if (started && depth <= 0) { i = j; break; }
                }
                add(steps, kind === "exit" ? "WaitForProcessExit" : "WaitForProcessStart",
                    { processName: proc, timeoutSeconds: timeout, label });
                label = ""; continue;
            }
            if (/^&\s*\{/.test(line)) {
                const { body, end } = collectBlock(lines, i);
                add(steps, "RunPowerShell", { script: body, waitForExit: true, label });
                i = end; label = ""; continue;
            }
            if (/^Start-Job\s+-ScriptBlock\s*\{/i.test(line)) {
                const { body, end } = collectBlock(lines, i);
                add(steps, "RunPowerShell", { script: body, waitForExit: false, label });
                i = end; label = ""; continue;
            }

            warnings.push(`Couldn't parse: ${line}`);
            label = "";
        }
        return { name: extractName(text, /^#\s*(.+)$/), steps, warnings };
    }

    return { importAny, detectFormat };
})();
